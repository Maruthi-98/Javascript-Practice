// using arrow function in Javascript


const arrowFunction = ()=>{
    console.log("Hello this is a arrow function")
}

arrowFunction();

// adding two numbers using arrow function

const addingTwoNumbers = (value1, value2)=>{
    return value1+value2;
}

console.log(addingTwoNumbers(1,2));

// adding three numbers using arrow function

const addingThreeNumbers = (value1,value2, value3)=>{
    return value1+value2+value3;
}

console.log(addingThreeNumbers(1,2,3));
