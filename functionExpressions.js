// working with function expression basics


const addTwoNumbers = function(value1, value2){
   return value1+value2;
}

console.log(addTwoNumbers(1,2));


const addThreeNumbers = function(value1, value2, value3){
   return value1+value2+value3;
}

const totalValue = addThreeNumbers(1,2,3);
console.log(totalValue);