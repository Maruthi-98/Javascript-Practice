// using functions in javascript

// adding 2 numbers

function addTwoNumbers(value1, value2){
    return value1+value2;
}

const totalValue = addTwoNumbers(1,2);

console.log(totalValue);


// adding 3 numbers using function

function addThreeNumbers(value1, value2, value3){
    return value1+value2+value3;
}

const returnTotal = addThreeNumbers(1,2,3);
console.log(returnTotal);

